//Variables

//var -> Global

var age = 22;
var num1 = 10;
var num2 = 11;

console.log(age)

console.log(num1 + num2)

//Naming convention for variables -> use camel case

var firstName = "Kamal"
console.log(firstName)


//let -> Local

if (true) {
    let a = 5;
    console.log(a);
}

//const -> local Note: once value is assigned to const, the value cannot be reassigned

const x = 25;

console.log(x)

//Variables practice
let a = "Kamal";
let b = "Baraili";

console.log(a + ' ' + b);
